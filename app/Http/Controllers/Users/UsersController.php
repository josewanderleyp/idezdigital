        $q = $request->input('q');

        if( isset($q) ){
            $q = $request->input('q');
            $data  = User::orderBy('created_at','desc')
               ->where('name', 'LIKE', "%{$q}%")
               ->orWhere('cpf', 'LIKE', "%{$q}%")
               ->paginate($this->perPage);
        }else{
            $data  = User::orderBy('created_at','desc')->paginate($this->perPage);
        }

        $title = 'Accounts';
        return view('users.index', compact('title', 'data'));
    }

